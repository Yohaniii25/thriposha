                                <div class="text">

                                    <p style="text-align:justify;">The Thriposha Program, now known as Sri Lanka Thriposha Limited (SLTL), has a rich history rooted in the mission to eradicate malnutrition in Sri Lanka. The initiative began in 1973 with the support of the CARE Humanitarian Organization from the USA, aiming to provide a nutritional supplement to vulnerable populations. Initially, the supplement, composed primarily of maize, soya, and milk powder, was imported from the USA, packaged in Sri Lanka, and then distributed to the targeted beneficiaries.<br>
In 1976, the Sri Lankan Ministry of Health recognized the program's importance and extended its support. To ensure efficient management and distribution, the Ceylon Tobacco Company (CTC) was selected through a competitive tender process to oversee the program.<br>
The significant evolution of the Thriposha Program occurred in 2010, when it was transformed into a fully government-owned entity under the Companies Act No. 07 of 2007, becoming Sri Lanka Thriposha Limited (SLTL). This transition marked the integration of the program under the direct purview of the Ministry of Health, reinforcing the government's commitment to combating malnutrition.<br>
Since its inception, Thriposha has maintained its core ingredients—maize, soya, and milk powder—ensuring a consistent and reliable nutritional profile. Over the years, SLTL has continued to innovate and expand its product offerings, like the introduction of Suposha, to reach a broader audience and further its mission of improving public health through superior nutrition.<br>
Sri Lanka Thriposha Limited (SLTL) remains steadfast in its mission to eradicate malnutrition and improve the overall health of the nation. As we look to the future, our strategic initiatives focus on innovation, sustainability, and expanding our reach to ensure every Sri Lankan has access to vital nutrition.<br>
We are committed to continually improving our product formulations to meet the evolving nutritional needs of our population. By integrating the latest scientific research and nutritional advancements, we aim to enhance the efficacy of our supplements, such as the recently introduced Suposha, ensuring they provide optimal health benefits.<br>
Moving forward, Sri Lanka Thriposha Limited is dedicated to building a healthier future for all Sri Lankans. Through innovation, sustainability, and unwavering commitment to our mission, we will continue to be the trusted provider of nutritional solutions in the fight against malnutrition.

