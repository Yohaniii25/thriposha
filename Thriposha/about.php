                                        The Thriposha Program was initiated with a clear and noble objective: to combat malnutrition across Sri Lanka. Initially, additional food supplements were imported from the USA and packaged locally before being distributed to the targeted beneficiaries. From its inception, Thriposha has been composed of essential ingredients: maize, soya, and milk powder.<br>
                                        In 1976, the Ministry of Health extended its support to the program, ensuring its sustained growth and outreach. The management of the program was awarded to Ceylon Tobacco Company (CTC) through a tender process, marking a significant collaboration in the fight against malnutrition.<br>
                                        In 2010, the Thriposha Program was transformed into a fully government-owned entity, now known as Sri Lanka Thriposha Limited (SLTL). This transition placed SLTL under the direct purview of the Ministry of Health, reinforcing our commitment to public health and nutrition.<br>
                                        With the enhancement of our production capabilities following the replacement of our extruder in 2016, SLTL was able to launch a new commercial product, “Suposha,” in 2017. This product marked a significant milestone, showcasing our ability to innovate and expand our offerings.<br>
                                        Continuing our journey of innovation, in 2023, SLTL proudly introduced another commercial product, the “Calorie Bar.” This addition reflects our ongoing efforts to utilize our capacity for the benefit of a broader market while staying true to our mission of improving nutrition.<br>
                                        At SLTL, we are dedicated to improving the nutritional status of our nation. Our journey from importing and packaging supplements to becoming a fully-fledged state enterprise producing and launching commercial products highlights our unwavering commitment to health and nutrition. We continue to strive towards our goal of eradicating malnutrition in Sri Lanka, ensuring a healthier future for all.


