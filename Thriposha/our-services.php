            <p>Quality Control Section functions under the Manager (Quality Preservation). Thriposha additional food which is used as an additional food for pregnant mothers,breast feeding mothers, infants and patients etc., should be prepared cleanly and with a proper standard under strict quality inspections. Therefore, the Quality Control Section should perform a massive role for this purpose under constant inspections.</p>

            <p>First of all, raw materials including maize, soya beans etc., required for Thriposha production are thoroughly checked for proper standard in taking over to the manufactory from suppliers. The checking also includes microbe testing, physical testing and other testing in the Laboratory.</p>

            <p>There are many instances when the containers of substandard raw materials brought to be produced to the Thriposha Company happen to be returned for the reason that such raw materials are proved to be not up to the standard expected. The process of production of Thriposha additional food is started by using raw materials in proper standard. Frequent sample testing is done at the Laboratory and Thriposha, in very good condition, is ultimately transferred to the Packing Section. Quality testing process runs up to the final stage of issuing Thriposha additional food to be distributed to beneficiaries. It is as a result of this process that Thriposha has become a fresh, tasty, nutritious and splendid additional food in high standard.</p>

